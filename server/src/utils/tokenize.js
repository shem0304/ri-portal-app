export function normalizeText(s) {
  return String(s || '')
    .replace(/[\u0000-\u001f]/g, ' ')
    .replace(/[\(\)\[\]\{\}<>"'`]/g, ' ')
    .replace(/[\.,;:!?·…]/g, ' ')
    .replace(/[\/\\]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

export function tokenizeKoreanTitle(title, { minLen = 2, stopwords = DEFAULT_STOPWORDS } = {}) {
  const txt = normalizeText(title);
  if (!txt) return [];

  // Split by whitespace; keep Korean, alnum, and a few connectors.
  const raw = txt.split(' ')
    .map(t => t.replace(/[^0-9A-Za-z가-힣_\-]/g, ''))
    .map(t => t.toLowerCase())
    .filter(Boolean);

  const tokens = [];
  for (const t of raw) {
    if (t.length < minLen) continue;
    if (stopwords.has(t)) continue;
    // Filter pure numbers
    if (/^\d+$/.test(t)) continue;
    tokens.push(t);
  }
  return tokens;
}

export function uniq(arr) {
  return Array.from(new Set(arr));
}
